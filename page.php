                <h1>The Normal Process of Fertilization</h1>


                <h3>For a couple to conceive a number of processes must take place successfully in both the man and the woman; and, at exactly the right time. The following briefly outlines the steps necessary for conception to take place:</h3>

                <ul>
                    <li><p>During the menstrual cycle, a woman’s hormones regulate the growth, maturation and release of an egg from her ovary. Each month the pituitary gland located in a woman’s brain sends a hormonal signal to her ovaries to prepare an egg for ovulation. These hormones are known as follicle-stimulating hormone (FSH) and luteinizing hormone (LH).</p></li>
                    <li><p>The pituitary gland is, in turn, controlled by an area of the woman’s brain known as the hypothalamus, which sends out a hormone known as GnRH, or gonadotropin-releasing hormone. GnRH “tells” the pituitary gland when to release FSH and LH.</p></li>
                    <li><p>An increase in FSH stimulates the growth of the developing follicles (and eggs) in the ovary.</p></li>
                    <li><p>A large increase in LH signals the ovary to release an egg—this is known as ovulation. Normally, only one of a woman’s two ovaries produces an egg during the woman’s menstrual cycle.</p></li>
                    <li><p>The released egg then moves from the ovary into the fallopian tube. The egg remains viable for about 24 hours.</p></li>
                    <li><p>For fertilization to occur a sperm must locate and penetrate the awaiting egg while it is in the fallopian tube.</p></li>
                    <li><p>If fertilization occurs, the fertilized egg, or embryo, continues to travel down the fallopian tube into the uterus, where it will attach, or implant, into the lining of the uterus.</p></li>
                </ul>


                <h3>In order for a couple to conceive, both the male and female reproductive systems must be functioning properly.</h3>

                <h4>In the female this means:</h4>

                <ul>
                    <li><p>Ovulation occurs and leads to the production of a viable egg.</p></li>
                    <li><p>The fallopian tubes are open and functioning properly to allow the egg and sperm to meet.</p></li>
                    <li><p>The fertilized egg is able to move into the uterus and is not blocked from implanting in the wall of the uterus.</p></li>
                    <li><p>The sperm are able to travel through the woman’s vagina, cervix, uterus, and fallopian tubes without being blocked in their attempt to locate the egg.</p></li>
                </ul>

                <h4>In the male this means:</h4>

                <ul>
                    <li><p>The testes produce viable, or normal sperm, as well as testosterone, the male hormone.</p></li>
                    <li><p>Ability to achieve an erection and engage in sexual intercourse.</p></li>
                    <li><p>Normal ejaculation occurs with semen going through the man’s urethra into the vagina.</p></li>
                    <li><p>The sperm that are produced are properly shaped, able to move rapidly, and can accurately move to the fallopian tubes to locate and fertilize the egg.</p></li>
